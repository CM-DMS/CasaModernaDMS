def _lookup_by_code(voucher_code: str):
    """Find a CM Voucher document by its printed code (strips dashes/spaces)."""
    clean = voucher_code.replace("-", "").replace(" ", "").upper()
    name = frappe.db.get_value("CM Voucher", {"voucher_code": clean}, "name")
    if not name:
        frappe.throw(
            f"No voucher found with code '{voucher_code}'. "
            "Please check the code and try again.",
            frappe.DoesNotExistError,
        )
    return frappe.get_doc("CM Voucher", name)


@frappe.whitelist()
def validate_voucher_for_payment(
    voucher_code: str,
    customer: str = "",
    amount: float = 0,
) -> dict:
    """Look up a voucher by its printed code and validate it for use as payment.

    Returns the voucher details if valid; raises a descriptive error if not.
    This is a read-only check — no state changes are made.

    If ``customer`` is provided the voucher's ``recipient_customer`` must match;
    a clear error is raised if it belongs to a different customer account.
    """
    _require_login()

    doc = _lookup_by_code(voucher_code)

    if doc.status != "Authorized":
        frappe.throw(
            f"This voucher is not available for payment. Current status: {doc.status}.",
            frappe.ValidationError,
        )

    if getdate(doc.valid_until) < getdate(today()):
        frappe.throw(
            f"This voucher expired on {frappe.format_date(doc.valid_until)} "
            "and can no longer be used.",
            frappe.ValidationError,
        )

    if customer and doc.recipient_customer != customer:
        frappe.throw(
            "This voucher belongs to a different customer account. "
            "Gift vouchers cannot be transferred or redeemed for cash.",
            frappe.PermissionError,
        )

    if amount and float(amount) > float(doc.voucher_value):
        frappe.throw(
            f"Payment amount (€{float(amount):,.2f}) exceeds the voucher "
            f"face value (€{float(doc.voucher_value):,.2f}).",
            frappe.ValidationError,
        )

    from frappe.utils import date_diff
    days_left = date_diff(doc.valid_until, today())

    return {
        "name":               doc.name,
        "voucher_code":       doc.voucher_code,
        "voucher_source":     doc.voucher_source or "Customer Purchase",
        "voucher_value":      doc.voucher_value,
        "valid_until":        str(doc.valid_until),
        "days_remaining":     days_left,
        "status":             doc.status,
        "recipient_customer": doc.recipient_customer,
        "recipient_name":     doc.recipient_name or "",
        "purchaser_customer": doc.purchaser_customer or "",
        "purchaser_name":     doc.purchaser_name or "",
    }


