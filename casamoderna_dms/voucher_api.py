@frappe.whitelist()
def redeem_voucher_by_code_with_pe(
    voucher_code: str,
    customer: str,
    amount: float,
    party_name: str = "",
    posting_date: str = "",
) -> dict:
    """Redeem a voucher by code AND create a submitted Payment Entry in one call.

    Creates a Payment Entry (payment_type=Receive, mode_of_payment=Gift Voucher,
    paid_to=Gift Vouchers - CM) so that the transaction appears in the Receipts
    list alongside cash/card/bank payments.

    Returns a dict with both ``pe_name`` and voucher redemption fields.
    """
    _require_login()

    # ── 1. Validate everything before any writes ──────────────────────────────
    voucher_doc = _lookup_by_code(voucher_code)
    amount = float(amount)

    if voucher_doc.status != "Authorized":
        frappe.throw(
            f"Only Authorized vouchers can be redeemed. "
            f"Current status: {voucher_doc.status}.",
            frappe.ValidationError,
        )
    if getdate(voucher_doc.valid_until) < getdate(today()):
        frappe.throw(
            f"This voucher expired on {frappe.format_date(voucher_doc.valid_until)}.",
            frappe.ValidationError,
        )
    if voucher_doc.recipient_customer != customer:
        frappe.throw(
            "This voucher is issued to a different customer account.",
            frappe.PermissionError,
        )
    if amount <= 0:
        frappe.throw("Redemption amount must be greater than zero.", frappe.ValidationError)
    if amount > float(voucher_doc.voucher_value):
        frappe.throw(
            f"Redemption amount (€{amount:,.2f}) exceeds voucher value "
            f"(€{float(voucher_doc.voucher_value):,.2f}).",
            frappe.ValidationError,
        )

    pdate = posting_date or today()

    # ── 2. Create and submit the Payment Entry ────────────────────────────────
    pe = frappe.get_doc({
        "doctype":                    "Payment Entry",
        "naming_series":              "PE .######",
        "company":                    "Casa Moderna Limited",
        "payment_type":               "Receive",
        "party_type":                 "Customer",
        "party":                      customer,
        "party_name":                 party_name or customer,
        "paid_amount":                amount,
        "received_amount":            amount,
        "base_paid_amount":           amount,
        "base_received_amount":       amount,
        "source_exchange_rate":       1,
        "target_exchange_rate":       1,
        "mode_of_payment":            "Gift Voucher",
        "paid_from":                  "Debtors - CM",
        "paid_from_account_currency": "EUR",
        "paid_to":                    "Gift Vouchers - CM",
        "paid_to_account_currency":   "EUR",
        "reference_no":               pdate,
        "reference_date":             pdate,
        "posting_date":               pdate,
        "remarks":                    f"Gift Voucher redemption — code: {voucher_code}",
    })
    pe.insert(ignore_permissions=True)
    pe.submit()

    # ── 3. Mark voucher as Redeemed, record PE name as reference ─────────────
    voucher_doc.status = "Redeemed"
    voucher_doc.redeemed_amount = amount
    voucher_doc.redeemed_date = now_datetime()
    voucher_doc.redeemed_against_so = pe.name
    voucher_doc.save()
    frappe.db.commit()

    return {
        "pe_name":           pe.name,
        "voucher_name":      voucher_doc.name,
        "status":            voucher_doc.status,
        "redeemed_amount":   amount,
        "redeemed_date":     str(voucher_doc.redeemed_date),
        "receipt_reference": pe.name,
    }
